package classes;

public class Ride {
	
	private int id;
	private String departure ;
	private String destination;
	private String date;
	private String time;
	private String price;
	private String comment;
	private String state;
	private String firstname;
	private String lastname;
	private String phone;
	private String brand;
	private String model;
	private String stateCar;
	
	public Ride() {
		super();
	}
	
	public Ride(int id, String departure, String destination, String date, String time, String price, String comment,
			String state, String firstname, String lastname, String phone, String brand, String model,
			String stateCar) {
		super();
		this.id = id;
		this.departure = departure;
		this.destination = destination;
		this.date = date;
		this.time = time;
		this.price = price;
		this.comment = comment;
		this.state = state;
		this.firstname = firstname;
		this.lastname = lastname;
		this.phone = phone;
		this.brand = brand;
		this.model = model;
		this.stateCar = stateCar;
	}		
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getDeparture() {
		return departure;
	}

	public void setDeparture(String departure) {
		this.departure = departure;
	}

	public String getDestination() {
		return destination;
	}

	public void setDestination(String destination) {
		this.destination = destination;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public String getComment() {
		return comment;
	}

	public void setComment(String comment) {
		this.comment = comment;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getFirstname() {
		return firstname;
	}

	public void setFirstname(String firstname) {
		this.firstname = firstname;
	}

	public String getLastname() {
		return lastname;
	}

	public void setLastname(String lastname) {
		this.lastname = lastname;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getBrand() {
		return brand;
	}

	public void setBrand(String brand) {
		this.brand = brand;
	}

	public String getModel() {
		return model;
	}

	public void setModel(String model) {
		this.model = model;
	}

	public String getStateCar() {
		return stateCar;
	}

	public void setStateCar(String stateCar) {
		this.stateCar = stateCar;
	}
	
	
	





	
;




}
